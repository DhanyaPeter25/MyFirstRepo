package collectionExample;

public class forEach {
//foreach loop is used mainly in arrays and collection
	//also called enhanced for loop
	public static void main(String[] args) 
	{
		String color[]={"black","red","blue","green"};
		for(String c:color) 
		{
		System.out.println(c);
		}
		int mark[]= {21,34,44,33};
        for(int c:mark)
        {
        	System.out.println(c);
        }
	}

}
